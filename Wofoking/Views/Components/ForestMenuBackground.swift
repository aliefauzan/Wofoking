    /// Linear-interpolate [angleDeg, offX, offY] at loop-fraction u (0...1),
    /// wrapping the final keyframe back to the first (both rest pose).
    private static func sample(_ kf: [Double], _ u: Double) -> (Double, Double, Double) {
        let n = kf.count / 4
        var i = 0
        while i < n - 1 && kf[(i + 1) * 4] <= u { i += 1 }
        let t0 = kf[i * 4]
        let hasNext = i + 1 < n
        let j = hasNext ? i + 1 : i
        let t1 = hasNext ? kf[j * 4] : 1
        let span = max(t1 - t0, 1e-6)
        let f = min(max((u - t0) / span, 0), 1)
        func lerp(_ k: Int) -> Double {
            let a = kf[i * 4 + k]
            let b = hasNext ? kf[j * 4 + k] : kf[i * 4 + k]
            return a + (b - a) * f
        }
        return (lerp(1), lerp(2), lerp(3))
    }

    var body: some View {
        GeometryReader { geo in
            let s = max(geo.size.width / 874, geo.size.height / 402)
            let ox = (geo.size.width - 874 * s) / 2
            let oy = (geo.size.height - 402 * s) / 2
            TimelineView(.animation) { tl in
                let now = tl.date.timeIntervalSinceReferenceDate
                let u = now.truncatingRemainder(dividingBy: Self.period) / Self.period
                ZStack {
                    ForEach(shown) { b in
                        let (angle, offX, offY) = Self.sample(b.kf, u)
                        Image(b.id)
                            .resizable()
                            .scaledToFill()
                            .frame(width: geo.size.width, height: geo.size.height)
                            .rotationEffect(
                                .degrees(angle),
                                anchor: UnitPoint(
                                    x: (ox + b.pivot.x * s) / geo.size.width,
                                    y: (oy + b.pivot.y * s) / geo.size.height))
                            .offset(x: offX * s, y: offY * s)
                    }
                }
            }
        }
        .ignoresSafeArea()
        .allowsHitTesting(false)
    }
}

